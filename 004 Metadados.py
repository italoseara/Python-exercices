a = input('Digite algo: ')
t = type(a)
s = a.isspace()
n = a.isnumeric()
al = a.isalpha()
an = a.isalnum()
ma = a.isupper()
mi = a.islower()
ca = a.istitle()
print('O tipo primitivo desse valor é: \033[1;31m{}\033[m'.format(t))
print('Só tem espaços? \033[1;31m{}\033[m'.format(s))
print('É um número? \033[1;31m{}\033[m'.format(n))
print('É alfabético? \033[1;31m{}\033[m'.format(al))
print('É alfanumérico? \033[1;31m{}\033[m'.format(an))
print('Está em maiusculo? \033[1;31m{}\033[m'.format(ma))
print('Está em minúsculo? \033[1;31m{}\033[m'.format(mi))
print('Está capitalizada? \033[1;31m{}\033[m'.format(ca))
